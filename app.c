#include "app.h"
#include "tester.h"
#include <stdio.h>
#include <stdlib.h>
#include <time.h>

/**
 * Entrada principal del sistema.
 *
 * @param argc cantidad de argumentos pasados en la linea de comandos.
 * @param argv argumentos de la linea de comandos.
 * @return {0} si el programa se ejecutó correctamente.
 */
int main(int argc, char** argv) {
    argv = &argv[1];
    time_t t;
    struct tm tm;
    t = time(NULL);
    tm = *localtime(&t);
    printf("\nStart date: %d-%02d-%02d %02d:%02d:%02d\n\n", tm.tm_year + 1900, tm.tm_mon + 1, tm.tm_mday, tm.tm_hour, tm.tm_min, tm.tm_sec);
    startTesting(argc, argv);
    t = time(NULL);
    tm = *localtime(&t);
    printf("\nEnd date:   %d-%02d-%02d %02d:%02d:%02d\n", tm.tm_year + 1900, tm.tm_mon + 1, tm.tm_mday, tm.tm_hour, tm.tm_min, tm.tm_sec);
    return 0;
}
